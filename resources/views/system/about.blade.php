      <h2>Informationen zum Projekt GSA-Online plus</h2>

      <h3>Das Projekt</h3>
      <p>
        Das Nachsorgeprogramm <em>GSA-Online plus</em> richtet sich an beruflich belastete Rehabilitanden bzw. Rehabilitanden mit Bedarf an berufsbezogenen Nachsorgeangeboten. Die internetbasierte Nachsorge erfolgt im direkten Anschluss an die stationäre Rehabilitation  und erstreckt sich über einen Zeitraum von 12 Wochen. Zentraler Bestandteil von <em>GSA-Online plus</em> ist das regelmäßige Verfassen von Tagebucheinträgen durch die Teilnehmer und eine zeitnahe Rückmeldung durch geschulte Onlinetherapeuten.
      </p>

      <h3>Fragestellung und Ziel des Projekts</h3>
      <p>
        Die Rückkehr an den Arbeitsplatz ist auch nach erfolgreicher Rehabilitation oft mit neuen Anforderungen oder Belastungen verbunden. Es besteht daher häufig Bedarf nach entsprechenden Nachsorgeprogrammen, welche dabei helfen, diesen Belastungen entgegenzuwirken und die Behandlungserfolge der stationären medizinischen Rehabilitation langfristig zu stabilisieren. Aus diesem Grunde haben wir ein internetbasiertes Nachsorgeprogramm entwickelt, welches beruflich belastete Rehabilitanden bei Ihrer Rückkehr an den Arbeitsplatz begleitet.
      </p>
      <p>
        Durch das Vorgängerprojekt <em>GSA-Online</em> konnte nachgewiesen werden, dass eine internetbasierte Nachsorgemaßnahme die Wiedereingliederung in den Arbeitsalltag von beruflich belasteten Patienten der stationären kardiologischen, orthopädischen und psychosomatischen Rehabilitation verbessern kann.
      </p>
      <p>
        Im aktuellen Projekt <em>GSA-Online plus</em> soll die Umsetzbarkeit der Online-Nachsorge in der Praxis bei Rehabilitanden verschiedener Indikationsbereiche überprüft werden. Hierfür wird das Programm an die Versorgungsabläufe der Rehabilitationseinrichtungen angepasst, so dass beruflich belastete Patienten in verschiedenen Rehabilitationskliniken einen einfachen und kostengünstigen Zugang zur Nachsorge erhalten können. Die Informationen über Ablauf, Inhalt und Nutzen von <em>GSA-Online plus</em> werden auf der Online-Plattform mittels ansprechender Filme vermittelt.
      </p>
      <p>
        Im Rahmen des laufenden Projekts soll überprüft werden, ob Teilnehmer von <em>GSA-Online plus</em> ihren positiven Umgang mit beruflichen Belastungen stabilisieren können und die Wiedereingliederung in den Beruf durch die Teilnahme an dem Projekt gefördert wird.
      </p>

      <h3>Die wöchentlichen Tagebucheinträge: Fokus der Schreibintervention</h3>
      <p>
        Die zentrale Aufgabe des Nachsorgeprogramm <em>GSA-Online plus</em> ist eine Schreibintervention: Die Teilnehmer verfassen jede Woche einen Tagebucheintrag zu einem vorgegebenen Schreibimpuls, welcher die Teilnehmer anleitet, über bestimmte Ereignisse, Gedanken und Gefühle zu schreiben. Zu Beginn des Nachsorgeprogramms können die Teilnehmer wählen, an welchem Wochentag sie ihren Eintrag regelmäßig schreiben möchten. <em>GSA-Online plus</em> kann somit problemlos in den jeweiligen Arbeitsalltag und Tagesablauf integriert werden. Die Kommunikation mit dem Onlinetherapeuten erfolgt anonym.
      </p>
      <p>
        Nach dem Schreiben des Eintrags liest ein geschulter Onlinetherapeut den Eintrag und verfasst in der Regel innerhalb von 24 Stunden eine persönliche Rückmeldung. Dabei geht der Therapeut auf die Belastungen und Probleme des Rehabilitanden ein, indem er Anregungen und Denkanstöße gibt. Das Ziel ist, dass Teilnehmer die von ihnen beschriebenen Situationen besser verstehen und auf Dauer besser mit belastenden Situationen umgehen können.
      </p>

      <h3>Theoretisches Konzept der Schreibinterventionen</h3>
      <p>
        Die Schreibintervention ist angelehnt an die <em>Supportiv-Expressive Therapie</em> (SET) nach Luborsky (1995), die auf der gemeinsamen Arbeit zwischen Teilnehmer und Therapeut basiert.
      </p>
      <p>
        Im Zentrum steht das <em>Zentrale Beziehungskonfliktthema (ZBKT):</em>
      </p>
      <p>
        Wenn Menschen miteinander in Kontakt stehen, stellt jeder Kontakt eine Beziehung dar. Der Fokus der Schreibinterventionen liegt auf den unterschiedlichen Beziehungen der Teilnehmer am Arbeitsplatz, beispielsweise mit Arbeitskollegen oder dem Chef.
      </p>
      <p>
        Der Ausgangspunkt dieses Beziehungskonzepts ist, dass alle Beziehungen nach einem sich wiederholenden Schema ablaufen. Dieses Schema teilt die beschriebenen Begegnungen bzw. Beziehungen der Rehabilitanden in drei Elemente auf, die der Onlinetherapeut anhand der Tagebucheinträge bestimmt:
      </p>
      <ol>
        <li>Der Wunsch, den der Teilnehmer an seinen Gegenüber hat [W].</li>
        <li>Die Reaktion des Gegenübers auf diesen Wunsch [RO].</li>
        <li>Die Reaktion, die der Teilnehmer wiederum auf die Reaktion seines Gegenübers zeigt [RS].</li>
      </ol>
      <p>
        Anders ausgedrückt verdeutlicht dieses Schema, dass der Wunsch bzw. die Erwartungen an eine Situation oder eine Person, sowohl die Reaktion des Gegenübers, als auch die eigene Reaktion beeinflussen.
      </p>

      <h4>Beispiel</h4>
      <p>
        <em>Frau A. kehrt nach einer vierwöchigen Reha an ihren Arbeitsplatz zurück. Im Rahmen der Online-Nachsorge berichtet sie, dass sie sich sehr darüber gefreut habe, dass so viele Kollegen sich nach ihrem Wohlbefinden erkundigt hätten. Das habe ihr den Einstieg sehr erleichtert und sie habe direkt mit der Vorgesetzten ihr Anliegen nach einer 4-Tage-Woche besprechen können.</em>
      </p>
      <p>
        Was war der Wunsch (W) von Frau A. in dieser Situation?
      </p>
      <p>
        (W): Verständnis und Rücksichtnahme für Ihre Erkrankung und ihre Sorgen.
      </p>
      <p>
        Wie hat das Gegenüber, in diesem Fall die Kollegen, auf den Wunsch von Frau A. reagiert (RO)?
      </p>
      <p>
        (RO): Die Kollegen haben sie freundlich begrüßt und sich nach ihrem Wohlbefinden erkundigt.
      </p>
      <p>
        Wie hat Frau A. wiederum auf die Reaktion ihrer Kollegen reagiert (RS)?
      </p>
      <p>
        (RS): Frau A. konnte ihr Anliegen, nicht mehr 5 Tage sondern nur noch 4 Tage arbeiten zu wollen, direkt bei der Vorgesetzten anbringen. Es war ihr möglich, ihre Bedürfnisse angemessen zu äußern.
      </p>

      <p>
        Um die Beziehungselemente W, RO und RS zu bestimmen, ist es wichtig, dass die Teilnehmer in ihrem Tagebuch von mehreren zwischenmenschlichen Ereignissen und Begegnungen berichten. Diese Art der Betrachtung kann dem Teilnehmer und dem Therapeuten helfen, das <em>Zentrale Beziehungskonfliktthema</em> (ZBKT) des Teilnehmers zu bestimmen. Darunter versteht man die für den Teilnehmer typische Gestaltung von Beziehungen, welche sich aus den häufigsten W, den häufigsten RO und den häufigsten RS (siehe oben) schließen lässt.
      </p>
      <p>
        Daraufhin erarbeiten Teilnehmer und Therapeut gemeinsam, inwieweit die individuelle Gestaltung von Beziehungen des Teilnehmers hilfreich oder problematisch sein kann. Damit können mögliche Lösungsalternativen für belastende Situationen gefunden werden.
      </p>

